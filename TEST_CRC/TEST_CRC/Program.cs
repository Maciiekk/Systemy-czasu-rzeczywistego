
using Modbus;

main();


void main()
{
    CRC crc = new CRC();

    // Example data to calculate CRC for
    string hexString = "1234567890AB"; // Example hexadecimal string
    byte[] data = CRC.HexStringToByteArray(hexString);

    // Calculate the CRC
    byte[] crcResult = crc.Calculate(data);

    // Convert CRC result to hexadecimal string
    string crcHexString = CRC.BytesToHex(crcResult);

    // Output the result
    Console.WriteLine("Data: " + hexString);
    Console.WriteLine("CRC: " + crcHexString);
}

namespace Modbus
{
    public class CRC
    {
        private int HiByte = 0xFF;
        private int LoByte = 0xFF;
        private int Index;

        public byte[] Calculate(byte[] data)
        {
            foreach (byte b in data)
            {
                Index = (HiByte ^ b) & 0xFF;
                HiByte = (LoByte ^ LookupTables.aCRCHi[Index]) & 0xFF;
                LoByte = LookupTables.aCRCLo[Index] & 0xFF;
            }

            return new byte[] { (byte)HiByte, (byte)LoByte };
        }

        public static byte[] HexStringToByteArray(string s)
        {
            int len = s.Length;
            byte[] data = new byte[len / 2];
            for (int i = 0; i < len; i += 2)
            {
                data[i / 2] = (byte)((Convert.ToByte(s[i].ToString(), 16) << 4)
                                     + Convert.ToByte(s[i + 1].ToString(), 16));
            }
            return data;
        }

        private static readonly char[] HEX_ARRAY = "0123456789ABCDEF".ToCharArray();

        public static string BytesToHex(byte[] bytes)
        {
            char[] hexChars = new char[bytes.Length * 2];
            for (int j = 0; j < bytes.Length; j++)
            {
                int v = bytes[j] & 0xFF;
                hexChars[j * 2] = HEX_ARRAY[v >> 4];
                hexChars[j * 2 + 1] = HEX_ARRAY[v & 0x0F];
            }
            return new string(hexChars);
        }
    }
}


