        private void StartButton_Click(object sender, RoutedEventArgs e)
        {
            string frameBytes = FrameBytesTextBox.Text.ToUpper().Replace(" ", "");

            if (string.IsNullOrWhiteSpace(frameBytes))
            {
                MessageBox.Show("Frame bytes cannot be empty.");
                return;
            }

            if (frameBytes.Length % 2 != 0)
            {
                // Insert "0" as the second character from the right
                frameBytes = frameBytes.Insert(frameBytes.Length - 1, "0");
            }

            if (!IsHexString(frameBytes))
            {
                MessageBox.Show("Invalid frame bytes. It should only contain hexadecimal characters (0-9, A-F).");
                return;
            }

            byte[] data = CRC.HexStringToByteArray(frameBytes);

            if (data.Length < 1 || data.Length > 32)
            {
                MessageBox.Show("Frame bytes should represent between 4 and 256 bits (1 to 32 bytes).");
                return;
            }

            if (!int.TryParse(RepetitionsTextBox.Text, out int repetitions))
            {
                MessageBox.Show("Invalid number of repetitions.");
                return;
            }

            CRC crc = new CRC();
            byte[] crcResult = null;

            // worm up execution
            crc.Calculate(data);

            Stopwatch stopwatch = new Stopwatch();
            stopwatch.Start();

            for (int i = 0; i < repetitions; i++)
            {
                crcResult = crc.Calculate(data);
            }

            stopwatch.Stop();
            double iterationTime = stopwatch.Elapsed.TotalMilliseconds / repetitions;

            TotalTimeTextBlock.Text = $"{stopwatch.Elapsed.TotalMilliseconds} ms";
            IterationTimeTextBlock.Text = $"{iterationTime} ms";
            string crcHexString = CRC.BytesToHex(crcResult);
            ChecksumTextBlock.Text = crcHexString;
        }

        private bool IsHexString(string input)
        {
            return input.All(c => "0123456789ABCDEF".Contains(c));
        }

